<template>
  <div class="animated fadeIn">
    <b-card>
      <b-form v-on:submit.prevent="onSubmit" v-on:reset.prevent="onReset" novalidate>
        <b-row>
          <b-col sm="4">
            <b-form-group label="Transaction Type" label-for="transtype" :state="chkState('transtypes')" >
              <v-select :options="transtypeList" multiple v-model="$v.form.transtypes.$model" placeholder="Select Transaction Type"></v-select>
            </b-form-group>
          </b-col>
          <b-col sm="4">
            <b-form-group label="From" :state="chkState('fromDate')">
              <v-date-picker
                v-model="$v.form.fromDate.$model"
                :input-props='{ class: "form-control" }'
                show-caps>
              </v-date-picker>
            </b-form-group>
          </b-col>
          <b-col sm="4">
            <b-form-group label="To" :state="chkState('toDate')">
              <v-date-picker
                v-model="$v.form.toDate.$model"
                :input-props='{ class: "form-control" }'
                show-caps>
              </v-date-picker>
            </b-form-group>
          </b-col>
        </b-row>
        <b-row>
          <b-col sm="4">
            <b-btn type="submit" variant="success" :disabled="$v.form.$invalid">Submit</b-btn>
          </b-col>
        </b-row>
      </b-form>
    </b-card>
  </div>
</template>

<script>
  import Vue from 'vue'
  import { mapState, mapActions } from 'vuex'
  import { required } from "vuelidate/lib/validators"
  import vSelect from 'vue-select'
  import { setupCalendar, DatePicker } from 'v-calendar'
  import 'v-calendar/lib/v-calendar.min.css'

  const formShape = {
    transtypes: [],
    fromDate: new Date(),
    toDate: new Date()
  }

  export default {
    name: 'ReportSearchTranstype',
    components: {
      vSelect,
      'v-date-picker': DatePicker
    },
    computed: {
      isValid() { return !this.$v.form.$anyError },
      isDirty() { return this.$v.form.$anyDirty },
      ... mapState({
        transtypes: state => state.transtypes.all
      })
    },
    watch: {
      transtypes: function(values) {
        this.transtypeList = []
        Vue._.forEach(values, (transtype) => {
          this.transtypeList.push({label: transtype.transtypename, value: transtype.transtypeid})
        });
        this.form.transtypes = this.transtypeList
      }
    },
    data: function () {
      return {
        submitted: false,
        selectedStore: null,
        from: '',
        to: '',
        form: Object.assign({}, formShape),
        transtypeList: [],
      }
    },
    validations: {
      form: {
        transtypes: {},
        fromDate: {
          required
        },
        toDate: {
          required
        }
      }
    },
    methods: {

      onSubmit() {
        if (this.validate()) {
          this.$nextTick(() => {
            let params = {
              transtypes: this.$v.form.transtypes.$model,
              fromDate: this.$moment(this.$v.form.fromDate.$model).format('YYYY-MM-DD'),
              toDate: this.$moment(this.$v.form.toDate.$model).format('YYYY-MM-DD')
            }
            this.$emit('search', params)
          })
        }
      },
      onReset() {
        this.form = formShape
        this.$nextTick(() => {
          this.$v.$reset()
        })
      },
      chkState(val) {
        const field = this.$v.form[val]
        return field.$dirty ? !field.$invalid : null
      },
      findFirstError(component = this) {
        if (component.state === false) {
          if (component.$refs.input) {
            component.$refs.input.focus()
            return true
          }
          if (component.$refs.check) {
            component.$refs.check.focus()
            return true
          }
        }
        let focused = false
        component.$children.some((child) => {
          focused = this.findFirstError(child)
          return focused
        })

        return focused
      },
      validate() {
        this.$v.$touch()
        this.$nextTick(() => this.findFirstError())
        return this.isValid
      }
    },
    created () {
      this.$store.dispatch('transtypes/getTranstypes')
    },
  }
</script>
