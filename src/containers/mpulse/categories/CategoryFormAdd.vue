<template>
<div class="animated fadeIn">
  <b-card no-body>
    <b-card-body>
      <b-row>
        <b-col lg="12">
          <!--<h6>Simple Form</h6>-->
          <b-form v-on:submit.prevent="onSubmit" v-on:reset.prevent="onReset" novalidate>

            <b-form-group label="Category Name" label-for="categoryName">
              <b-form-input id="categoryName" type="text" aria-describedby="liveFeedbackName" v-model.lazy.trim="$v.form.categoryName.$model" placeholder="Category Name" autofocus />
              <b-form-invalid-feedback id="liveFeedbackName">
                This is a required field.
              </b-form-invalid-feedback>
            </b-form-group>

            <b-form-group label="Est Profit" label-for="estprofit">
              <b-form-input id="estProfit" type="number" aria-describedby="liveFeedbackEstProfit" v-model.lazy.trim="$v.form.estProfit.$model" placeholder="Estimate Profit" autofocus />
              <b-form-invalid-feedback id="liveFeedbackEstProfit">
                This is a required field.
              </b-form-invalid-feedback>
            </b-form-group>

            <b-form-group>
              <div class="custom-control custom-control-inline">
                <b-form-checkbox id="isVisible"  v-model.lazy.trim="$v.form.isVisible.$model" :unchecked-value="false">
                  Is Visible
                </b-form-checkbox>
              </div>
              <div class="custom-control custom-control-inline">
                <b-form-checkbox id="isActive" v-model.lazy.trim="$v.form.isActive.$model" :unchecked-value="false">
                  Is Active
                </b-form-checkbox>
              </div>
            </b-form-group>

            <b-button type="submit" variant="primary" :disabled="$v.form.$invalid">
              Submit
            </b-button>
            <b-button class="ml-1" type="reset" @click="onCancel">Cancel</b-button>
          </b-form>
          <br />
        </b-col>
      </b-row>
    </b-card-body>
  </b-card>
</div>
</template>

<script>
import Vue from 'vue'
import {
  mapState,
  mapActions
} from 'vuex'
import {
  required
} from "vuelidate/lib/validators"

const formShape = {
  categoryName: "",
  estProfit: null,
  isVisible: false,
  isActive: true
}

export default {
  name: "CategoryFormAdd",
  data() {
    return {
      form: Object.assign({}, formShape),
      submitted: false
    }
  },
  computed: {
    isValid() {
      return !this.$v.form.$anyError
    },
    isDirty() {
      return this.$v.form.$anyDirty
    },
  },
  validations: {
    form: {
      categoryName: {
        required
      },
      estProfit: {
        required
      },
      isVisible:{},
      isActive: {}
    }
  },
  methods: {
    onSubmit() {
      if (this.validate()) {
        this.$nextTick(() => {
          this.submitted = true
          let params = {
            categoryName: this.$v.form.categoryName.$model,
            estProfit: this.$v.form.estProfit.$model,
            isActive: this.$v.form.isActive.$model,
            isVisible: this.$v.form.isVisible.$model
          }

          this.$store.dispatch('categories/addCategory', params)
            .then(() => {
              this.$emit('success')
              this.showSuccessMsg()
            })
            .catch((error) => {
              if (error.response.status == 400) {
                this.showErrorMsg({message: error.response.data.message })
              } else {
                this.showErrorMsg()
              }
            })
        })
      }
    },
    onCancel() {
      this.$emit('cancel')
    },
    onReset() {
      // Reset validation
      this.form = formShape
      this.submitted = false
      this.$nextTick(() => {
        this.$v.$reset()
      })
    },
    chkState(val) {
      const field = this.$v.form[val]
      return field.$dirty ? !field.$invalid : null
    },
    findFirstError(component = this) {
      if (component.state === false) {
        if (component.$refs.input) {
          component.$refs.input.focus()
          return true
        }
        if (component.$refs.check) {
          component.$refs.check.focus()
          return true
        }
      }
      let focused = false
      component.$children.some((child) => {
        focused = this.findFirstError(child)
        return focused
      })

      return focused
    },
    validate() {
      this.$v.$touch()
      this.$nextTick(() => this.findFirstError())
      return this.isValid
    }
  }
}
</script>
